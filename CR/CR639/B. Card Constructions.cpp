int size2cards(int size) {
  return size * (3 * size + 1) / 2;
}


template <class T>
T find_max(T l, T r, bool f(T)) {
  T c;
  while (r - l > 1) {
    c = (r + l) / 2;
    if (f(c)) {
      l = c;
    } else {
      r = c;
    }
  }

  return size2cards(l);
}

int n;

bool f(int trial) {
  return size2cards(trial) <= n;
}

